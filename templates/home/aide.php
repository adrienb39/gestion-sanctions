            <h1 class="display-4">Section d'Aide</h1>
            <p class="lead">Bienvenue dans la section d'aide. Voici les réponses aux questions fréquemment posées et des instructions détaillées sur l'utilisation de notre site.</p>

            <h3>1. Comment créer un compte ?</h3>
            <p>Pour créer un compte, cliquez sur le bouton <strong>"Créer un compte"</strong> sur la page d'accueil. Vous devrez fournir votre nom, prénom, et une adresse email valide. Une fois inscrit, vous recevrez un email de confirmation pour valider votre compte.</p>

            <h3>2. Comment me connecter ?</h3>
            <p>Pour vous connecter, cliquez sur le bouton <strong>"Se connecter"</strong> en haut à droite du site. Entrez votre email et votre mot de passe. Si vous avez oublié votre mot de passe, vous pouvez le réinitialiser en cliquant sur <strong>"Mot de passe oublié"</strong> sur la page de connexion.</p>

            <h3>3. Comment consulter mes sanctions ?</h3>
            <p>Une fois connecté, allez dans la section <strong>"Mes sanctions"</strong> dans le menu. Vous y trouverez la liste des sanctions que vous avez reçues, ainsi que les détails associés (type de sanction, date, etc.).</p>

            <h3>4. Comment demander une révision de ma sanction ?</h3>
            <p>Si vous estimez qu'une sanction est injustifiée, vous pouvez demander une révision. Pour ce faire, cliquez sur la sanction concernée, puis sur le bouton <strong>"Demander une révision"</strong>. Vous devrez expliquer votre demande et soumettre les documents nécessaires pour appuyer votre réclamation.</p>

            <h3>5. Où puis-je consulter le règlement disciplinaire ?</h3>
            <p>Vous pouvez consulter le règlement disciplinaire en cliquant sur le lien <strong>"Règlement disciplinaire"</strong> dans le menu du site. Il contient les règles de comportement à suivre au sein de l'établissement et les sanctions correspondantes pour chaque type d'infraction.</p>

            <h3>6. Qui contacter si j'ai une question spécifique ?</h3>
            <p>Si vous avez une question ou une demande spécifique, vous pouvez nous contacter directement via le formulaire de contact situé sur la page <strong>"Contact"</strong>. Nous vous répondrons dans les plus brefs délais.</p>

            <h3>7. Que faire si je rencontre un problème technique ?</h3>
            <p>Si vous rencontrez un problème technique sur le site, vous pouvez nous en informer en envoyant un email à <a href="mailto:support@exemple.com">support@exemple.com</a>. N'oubliez pas de fournir le maximum de détails sur le problème rencontré.</p>

            <hr>
            <p>Si vous avez d'autres questions, n'hésitez pas à nous contacter ou à consulter la <a href="faq.php">FAQ</a> pour plus d'informations.</p>
